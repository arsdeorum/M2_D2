/* ESERCIZIO 1
 Elenca e descrivi i principali datatype in JavaScript. Prova a spiegarli come se volessi farli comprendere a un bambino.
*/

/* 
Stringa: Una stringa è un insieme di caratteri che possono essere lettere, numeri o simboli. È come un pezzo di testo che possiamo usare per scrivere parole o frasi. Ad esempio, "hello world!" è una stringa.

Numero: Un numero è un valore matematico. Possiamo usarlo per contare le cose o per fare calcoli come l'addizione o la sottrazione.

Booleano: Un booleano può avere solo due valori: vero o falso. Possiamo pensarci come a una risposta a una domanda, solo che la risposta potrebbe solo essere vero o falso.

Array: Un array è un insieme di valori separati da virgole e racchiusi tra parentesi quadre []. Possiamo pensarlo come a una scatola che contiene diverse cose. Ad esempio, un array potrebbe essere [1, 2, 3, 4, 5], che contiene una serie di numeri. 

Undefined: Se qualcosa è "undefined" significa che non ha un valore assegnato, si può pensarlo come a un'informazione mancante o a una casella vuota. Ad esempio, se mi chiedono quale sia il mio colore preferito e non rispondo, il valore sarà "undefined" perché non ho fornito una risposta.

Null: Null è simile a "undefined" nel senso che rappresenta l'assenza di valore, è un'indicazione che qualcosa non esiste o è vuoto.

Data: La data rappresenta un punto nel tempo, come il giorno corrente. Possiamo usarla per tenere traccia delle informazioni temporali.

*/

